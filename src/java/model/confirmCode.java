package model;

/**
 *
 * @author HP
 */
public class confirmCode {
    private String code;
    private String time;

    public confirmCode(String code, String time) {
        this.code = code;
        this.time = time;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
    
    
}
